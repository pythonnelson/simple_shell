#include "shell.h"

/**
 * interactive - this returns true if the shell is in interactive mode
 * @info: the struct address
 *
 * Return: 1 if in the interactive mode, else 0 otherwise
 */
int interactive(info_t *info)
{
	return (isatty(STDIN_FILENO) && info->readfd <= 2);
}

/**
 * is_delim - this checks if the character is a delimeter
 * @c: the char to be checked
 * @delim: this is the delimeter string
 * Return: returns 1 if true, else 0 if false
 */
int is_delim(char c, char *delim)
{
	while (*delim)
		if (*delim++ == c)
			return (1);
	return (0);
}

/**
 * _isalpha - this checks for alphabetic characters
 * @c: The character to input
 * Return: returns 1 if c is alphabetic, else 0 otherwise
 */

int _isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}

/**
 * _atoi - this converts a string to an integer
 * @s: this is the string to be converted
 * Return: return 0 if no number is in the string, else return converted number
 */

int _atoi(char *s)
{
	int i, sign = 1, flag = 0, output;
	unsigned int result = 0;

	for (i = 0; s[i] != '\0' && flag != 2; i++)
	{
		if (s[i] == '-')
			sign *= -1;

		if (s[i] >= '0' && s[i] <= '9')
		{
			flag = 1;
			result *= 10;
			result += (s[i] - '0');
		}
		else if (flag == 1)
			flag = 2;
	}

	if (sign == -1)
		output = -result;
	else
		output = result;

	return (output);
}
